    "# TODO: Implement Transformer generator\n",
    "class BeatSaberGenerator(nn.Module):\n",
    "    \"\"\"\n",
    "    Audio → Token sequence generator\n",
    "    \n",
    "    Architecture:\n",
    "    - Audio encoder: Transformer encoder over tick-aligned features\n",
    "    - Token decoder: Transformer decoder (autoregressive)\n",
    "    - Conditioning: Difficulty embedding\n",
    "    \"\"\"\n",
    "    def __init__(self, \n",
    "                 vocab_size=406,\n",
    "                 d_model=512,\n",
    "                 n_heads=8,\n",
    "                 n_encoder_layers=6,\n",
    "                 n_decoder_layers=6,\n",
    "                 d_audio=640,  # 128 mels * 5 context frames\n",
    "                 max_seq_len=4096,\n",
    "                 dropout=0.1):\n",
    "        super().__init__()\n",
    "        \n",
    "        self.d_model = d_model\n",
    "        \n",
    "        # Difficulty embedding (Easy=0, Normal=1, Hard=2, Expert=3, Expert+=4)\n",
    "        self.difficulty_embed = nn.Embedding(5, d_model)\n",
    "        \n",
    "        # Audio encoder\n",
    "        self.audio_proj = nn.Linear(d_audio, d_model)\n",
    "        self.audio_pos_encoding = PositionalEncoding(d_model, max_len=max_seq_len)\n",
    "        encoder_layer = nn.TransformerEncoderLayer(\n",
    "            d_model=d_model, nhead=n_heads, dim_feedforward=d_model*4, dropout=dropout, batch_first=True\n",
    "        )\n",
    "        self.audio_encoder = nn.TransformerEncoder(encoder_layer, num_layers=n_encoder_layers)\n",
    "        \n",
    "        # Token decoder\n",
    "        self.token_embed = nn.Embedding(vocab_size, d_model)\n",
    "        self.token_pos_encoding = PositionalEncoding(d_model, max_len=max_seq_len)\n",
    "        decoder_layer = nn.TransformerDecoderLayer(\n",
    "            d_model=d_model, nhead=n_heads, dim_feedforward=d_model*4, dropout=dropout, batch_first=True\n",
    "        )\n",
    "        self.token_decoder = nn.TransformerDecoder(decoder_layer, num_layers=n_decoder_layers)\n",
    "        \n",
    "        # Output projection\n",
    "        self.output_proj = nn.Linear(d_model, vocab_size)\n",
    "    \n",
    "    def forward(self, audio_features, token_ids, difficulty_level, tgt_mask=None):\n",
    "        \"\"\"\n",
    "        Args:\n",
    "            audio_features: [B, T_audio, D_audio]\n",
    "            token_ids: [B, T_tokens] (input sequence)\n",
    "            difficulty_level: [B] (0-4)\n",
    "            tgt_mask: [T_tokens, T_tokens] causal mask\n",
    "        \n",
    "        Returns:\n",
    "            logits: [B, T_tokens, vocab_size]\n",
    "        \"\"\"\n",
    "        # Encode audio\n",
    "        audio_embed = self.audio_proj(audio_features)  # [B, T_audio, d_model]\n",
    "        audio_embed = self.audio_pos_encoding(audio_embed)\n",
    "        \n",
    "        # Add difficulty conditioning (broadcast to all audio frames)\n",
    "        diff_embed = self.difficulty_embed(difficulty_level)  # [B, d_model]\n",
    "        audio_embed = audio_embed + diff_embed.unsqueeze(1)  # Broadcast\n",
    "        \n",
    "        memory = self.audio_encoder(audio_embed)  # [B, T_audio, d_model]\n",
    "        \n",
    "        # Decode tokens\n",
    "        token_embed = self.token_embed(token_ids)  # [B, T_tokens, d_model]\n",
    "        token_embed = self.token_pos_encoding(token_embed)\n",
    "        \n",
    "        if tgt_mask is None:\n",
    "            tgt_mask = self._generate_square_subsequent_mask(token_ids.size(1)).to(token_ids.device)\n",
    "        \n",
    "        decoder_out = self.token_decoder(\n",
    "            tgt=token_embed,\n",
    "            memory=memory,\n",
    "            tgt_mask=tgt_mask\n",

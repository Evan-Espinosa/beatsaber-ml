    "    Training:\n",
    "    - Pairwise ranking: L = -log(sigmoid(score_A - score_B))\n",
    "      where map A is preferred over map B\n",
    "    \"\"\"\n",
    "    def __init__(self, d_audio=640, d_map=32, d_model=256, n_layers=4):\n",
    "        super().__init__()\n",
    "        \n",
    "        # Project inputs\n",
    "        self.audio_proj = nn.Linear(d_audio, d_model)\n",
    "        self.map_proj = nn.Linear(d_map, d_model)\n",
    "        \n",
    "        # Transformer encoder\n",
    "        encoder_layer = nn.TransformerEncoderLayer(\n",
    "            d_model=d_model, nhead=8, dim_feedforward=d_model*4, batch_first=True\n",
    "        )\n",
    "        self.encoder = nn.TransformerEncoder(encoder_layer, num_layers=n_layers)\n",
    "        \n",
    "        # Pooling + scoring\n",
    "        self.score_head = nn.Sequential(\n",
    "            nn.Linear(d_model, d_model),\n",
    "            nn.ReLU(),\n",
    "            nn.Linear(d_model, 1)\n",
    "        )\n",
    "    \n",
    "    def forward(self, audio_features, map_features):\n",
    "        \"\"\"\n",
    "        Args:\n",
    "            audio_features: [B, T, D_audio]\n",
    "            map_features: [B, T, D_map]\n",
    "        \n",
    "        Returns:\n",
    "            scores: [B] scalar quality scores\n",
    "        \"\"\"\n",
    "        # Combine audio + map\n",
    "        audio_embed = self.audio_proj(audio_features)\n",
    "        map_embed = self.map_proj(map_features)\n",
    "        combined = audio_embed + map_embed  # [B, T, d_model]\n",
    "        \n",
    "        # Encode\n",
    "        encoded = self.encoder(combined)  # [B, T, d_model]\n",
    "        \n",
    "        # Pool (mean over time)\n",
    "        pooled = encoded.mean(dim=1)  # [B, d_model]\n",
    "        \n",
    "        # Score\n",
    "        scores = self.score_head(pooled).squeeze(-1)  # [B]\n",

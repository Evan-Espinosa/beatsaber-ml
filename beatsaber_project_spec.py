    "\n",
    "### What We're Building\n",
    "\n",
    "**Input:** Audio file (.ogg/.egg) + difficulty level (Hard/Expert/Expert+)\n",
    "\n",
    "**Output:** Beat Saber level files (.dat format) ready to play\n",
    "\n",
    "**Approach:** Two-stage system\n",
    "- **Generator:** Transformer that maps audio → note/obstacle sequence\n",
    "- **Scorer:** Neural network that ranks map quality (added Phase 3)\n",
    "- **Pipeline:** Generate N candidates → validate constraints → rerank by score → output best\n",
    "\n",
    "### Key Design Decisions\n",
    "\n",
    "| Decision | Choice | Rationale |\n",
    "|----------|--------|----------|\n",
    "| Format support | v2 initially, v3 later | 90%+ maps are v2; simpler to start |\n",
    "| BPM changes | Parse but filter for Phase 1 | Constant BPM is easier; add variable tempo Phase 2 |\n",
    "| Lights/events | Phase 3 only | Gameplay > visuals for MVP |\n",
    "| Quality signal | Upvote ratio (75%+ with 50+ votes) | Best proxy for player enjoyment |\n",
    "| Model architecture | Token-stream Transformer | Proven for sequence generation |\n",
    "| Training approach | Positive-only → contrastive | Simple first, sophistication later |\n",
    "| Target difficulties | Hard, Expert, Expert+ | Most played; single model conditioned on difficulty |\n",
